using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Threading;
using System.Xml.Linq;
using System.ComponentModel;
using System.Diagnostics.Eventing.Reader;


namespace consoleTextRPG
{
    internal class Program
    {
        static void Main(string[] args)
        {
            Warrior warrior = new Warrior("Воин", 120, 20, 0, 1, 0);
            Sorcerer sorcerer = new Sorcerer("Маг", 70, 60, 1, 1, 0);
            Slayer slayer = new Slayer("Убийца", 90, 30, 0, 1, 0);
            Archer archer = new Archer("Лучник", 80, 20, 1, 1, 0);


            Welcome();


            int chosenClass = playerPick(warrior, sorcerer, slayer, archer);


            BaseClass player = ClassFactory.CreateInstance(chosenClass);
            SlowWrite("Продолжение следует...");

            while (true)
            {
                Console.ReadKey();
            }


        }


        static void Welcome()
        {
            Console.CursorVisible = false;
            SlowWrite("Нажми любую клавишу чтобы начать...");
            SlowWrite("Привет!");
            SlowWrite("Это текстовая РПГ для моего обучения.");
            SlowWrite("Для начала выбери класс персонажа:");
        }

        static int playerPick(Warrior warrior, Sorcerer sorcerer, Slayer slayer, Archer archer)
        {
            ShowClassList(warrior, sorcerer, slayer, archer);

            bool inChoice = true;
            int chosenClass = 0;
            ConsoleKeyInfo PressedKey;
            while (inChoice)
            {
                PressedKey = Console.ReadKey();
                inChoice = ChoisingClass(warrior, sorcerer, slayer, archer, PressedKey, ref chosenClass);
            }


            switch (chosenClass)
            {
                case 1:
                    SlowWrite("Выбран Воин!");
                    break;

                case 2:
                    SlowWrite("Выбран Маг!");
                    break;
                case 3:
                    SlowWrite("Выбран Убийца!");
                    break;
                case 4:
                    SlowWrite("Выбран Лучник!");
                    break;
                default:
                    Console.WriteLine("Шота сломалось :(");
                    break;
            }
            return chosenClass;
        }

        static void SlowWrite(string str, ConsoleColor textColor = ConsoleColor.Yellow, bool needClear = true)
        {

            Console.ForegroundColor = textColor;
            if (needClear)
            {
                Console.Clear();

            }
            Console.Write("\n   ");
            foreach (char letter in str)
            {
                Console.Write(letter);
                Thread.Sleep(14);
            }

            if (needClear)
            {
                Console.ReadKey();

            }
        }

        static void ShowClassList(Warrior warrior, Sorcerer sorcerer, Slayer slayer, Archer archer)
        {
            Console.Clear();
            SlowWrite($"1. {warrior.Name}", ConsoleColor.Red, false);
            SlowWrite($"2. {sorcerer.Name}", ConsoleColor.Blue, false);
            SlowWrite($"3. {slayer.Name}", ConsoleColor.DarkRed, false);
            SlowWrite($"4. {archer.Name}", ConsoleColor.Green, false);
            Console.WriteLine();
            SlowWrite("Нажми соответствующую цифру для получения подробной информации о классе.", needClear: false);
        }

        static bool ChoisingClass(Warrior warrior, Sorcerer sorcerer, Slayer slayer, Archer archer, ConsoleKeyInfo pressedKey, ref int chosenClass)
        {

            string text = "\n   Нажмите Enter для выбора класса или любую другую клавишу чтобы вернуться к списку классов.";
            bool inChoice = true;
            switch (pressedKey.Key)
            {
                case ConsoleKey.D1:
                    warrior.ShowStats();
                    SlowWrite(text, needClear: false);
                    break;
                case ConsoleKey.D2:
                    sorcerer.ShowStats();
                    SlowWrite(text, needClear: false);
                    break;
                case ConsoleKey.D3:
                    slayer.ShowStats();
                    SlowWrite(text, needClear: false);
                    break;
                case ConsoleKey.D4:
                    archer.ShowStats();
                    SlowWrite(text, needClear: false);
                    break;
                default:
                    Console.Clear();
                    ShowClassList(warrior, sorcerer, slayer, archer);
                    return inChoice;
            }
            ConsoleKey secondKey = Console.ReadKey().Key;
            switch (secondKey)
            {
                case ConsoleKey.Enter:
                    if (pressedKey.Key == ConsoleKey.D1)
                    {
                        chosenClass = 1;
                        inChoice = false;
                        return inChoice;
                    }
                    else if (pressedKey.Key == ConsoleKey.D2)
                    {
                        chosenClass = 2;
                        inChoice = false;
                        return inChoice;
                    }
                    else if (pressedKey.Key == ConsoleKey.D3)
                    {
                        chosenClass = 3;
                        inChoice = false;
                        return inChoice;
                    }
                    else if (pressedKey.Key == ConsoleKey.D4)
                    {
                        chosenClass = 4;
                        inChoice = false;
                        return inChoice;
                    }
                    else
                    {
                        Console.Clear();
                        ShowClassList(warrior, sorcerer, slayer, archer);
                        break;
                    }
                default:
                    Console.Clear();
                    ShowClassList(warrior, sorcerer, slayer, archer);
                    break;
            }

            return inChoice;

        }

        class BaseClass
        {

            public string Name { get; private set; }
            public int HP { get; private set; }
            public int MP { get; private set; }
            public int AtcRange { get; private set; }
            public int Level { get; private set; }
            public int EXP { get; private set; }



            public BaseClass(string name, int hp, int mp, int atcRange, int level, int exp)
            {
                Name = name;
                HP = hp;
                MP = mp;
                AtcRange = atcRange;
                Level = level;
                EXP = exp;
            }

            public void ShowStats()
            {
                Console.Clear();
                SlowWrite($"{Name}\n", ConsoleColor.Yellow, false);
                SlowWrite($"Здоровье: {HP}", ConsoleColor.Yellow, false);
                SlowWrite($"Мана: {MP}", ConsoleColor.Yellow, false);
                if (AtcRange < 1)
                    SlowWrite("Ближний бой", ConsoleColor.Yellow, false);
                else
                    SlowWrite("Дальний бой", ConsoleColor.Yellow, false);


            }

            public void GainExp(int amountEXP)
            {
                EXP += amountEXP;
                while(EXP >= 10)
                {
                    EXP -= 10;
                    LevelUp();
                }
            }
            private void LevelUp()
            {
                Level += 1;
                HP += 5;
            }


        }

        class Warrior : BaseClass
        {
            public Warrior(string name, int hp, int mp, int atcRange, int level, int exp) : base(name, hp, mp, atcRange, level, exp)
            {

            }
        }

        class Sorcerer : BaseClass
        {
            public Sorcerer(string name, int hp, int mp, int atcRange, int level, int exp) : base(name, hp, mp, atcRange, level, exp)
            {

            }
        }

        class Slayer : BaseClass
        {
            public Slayer(string name, int hp, int mp, int atcRange, int level, int exp) : base(name, hp, mp, atcRange, level, exp)
            {

            }
        }

        class Archer : BaseClass
        {
            public Archer(string name, int hp, int mp, int atcRange, int level, int exp) : base(name, hp, mp, atcRange, level, exp)
            {

            }
        }

        class ClassFactory
        {
            public static BaseClass CreateInstance(int value)
            {
                if (value == 1)
                {
                    return new Warrior("Воин", 120, 20, 0, 1, 0);
                }
                else if (value == 2)
                {
                    return new Sorcerer("Маг", 70, 60, 1, 1, 0);
                }
                else if (value == 3)
                {
                    return new Slayer("Убийца", 90, 30, 0, 1, 0);
                }
                else if (value == 4)
                {
                    return new Archer("Лучник", 80, 20, 1, 1, 0);
                }
                else { throw new ArgumentException("Неверный класс"); }

            }
            
        }
    }
}








